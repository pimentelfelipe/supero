function setLanguage(id) {
    if (id != null && id != "") {
		var lang = id;
	    setCookie(langCookie, lang, 365);
	    
	    $('.translatable').each(function(i){
	      $(this).text(aLangKeys[lang][ $(this).attr('key') ]);
	    });
	    
	    $('.translatable-placeholder').each(function(i){
		  $(this).attr('placeholder',aLangKeys[lang][ $(this).attr('key') ]);
		});
	}
}


